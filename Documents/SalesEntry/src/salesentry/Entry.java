    void setList(){
        addedItems=db.getListItem();
        sales=db.getSales();  //Gets sales of the current date
        itemList.setModel(new javax.swing.AbstractListModel() {
            @Override
            public int getSize() {
                return addedItems.length; //To change body of generated methods, choose Tools | Templates.
            }

            @Override
            public Object getElementAt(int index) {
                return addedItems[index]; //To change body of generated methods, choose Tools | Templates.
            }
        });
        
        itemList.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(e.getValueIsAdjusting()){
                    no=itemList.getSelectedIndex();
                    for(dbPojo pojo : sales)
                        System.out.println(pojo.getItem());
                    pojo=sales.get(no);
                    edit=true;
                    add.setText("Edit");
                    exit.setText("Cancel");
                    ItemComboBox.setSelectedItem(pojo.getItem());
                    price.setText(pojo.getAmount()+"");
                    date.setText(pojo.getDate());
                    //Create a button to cancel the edit. Also create a boolean variable to know if its edit or to add.
                }//To change body of generated methods, choose Tools | Templates.
            }
        });
    }

