    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        dbPojo pojo=new dbPojo();
        String item=ItemComboBox.getSelectedItem().toString();
        int price=0;
        try{
            price=Integer.parseInt(this.price.getText().toString());
        }
        catch(Exception ex){
            System.err.println("---------->"+ex);
        }
        String date=this.date.getText().toString();
        pojo.setDate(date);
        pojo.setItem(item);
        pojo.setAmount(price);
        if(!edit){
            db.insertData(pojo);
        }
        else{
            pojo.setSno(no+1);
            pojo.setEditDate(Entry.pojo.getDate());
            db.update(pojo);
            edit=false;
            this.date.setText(todayDate);
            add.setText("Add");
            exit.setText("Exit");
        }
        setList();
        int total=db.getTodayTotal();
        this.total.setText(total+"");
        this.price.setText("");
    }//GEN-LAST:event_addActionPerformed

